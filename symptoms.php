<h1 class="text-center">Symptoms</h1>
<h2> How is it contracted? </h2>
<p>The virus is contracted from the bite of a striped Aedes aegypti mosquito that has previously bitten an infected person. The mosquito flourishes during rainy seasons but can breed in water-filled flower pots, plastic bags, and cans year-round. One mosquito bite can cause the disease.

The virus is not contagious and cannot be spread directly from person to person. It is mosquito-borne, so there must be a person-to-mosquito-to-another-person pathway. The full life cycle of the virus involves the mosquito as the vector (transmitter) and the human as the source of infection.
<br>
</p>
<p>Typically, people infected with dengue virus are asymptomatic (80%) or have only mild symptoms such as an uncomplicated fever.
 Others have more severe illness (5%), and in a small proportion it is life-threatening. The incubation period (time between exposure 
 and onset of symptoms) ranges from 3 to 14 days, but most often it is 4 to 7 days. Therefore, travelers returning from endemic areas 
 are unlikely to have dengue if fever or other symptoms start more than 14 days after arriving home.
 <br>Children often experience symptoms
  similar to those of the common cold and gastroenteritis (vomiting and diarrhea) and have a greater risk of severe complications, 
  though initial symptoms are generally mild but include high fever.
</p>
<img src="symptoms.jpg" align="right" width="400" height="500">
<h2>Clinical Course</h2>
<p>
  Symptoms, which usually begin four to six days after infection and last for up to 10 days, may include
<br>- Sudden, high fever
<br>- Severe headaches
<br>- Pain behind the eyes
<br>- Severe joint and muscle pain
<br>- Fatigue
<br>- Nausea
<br>- Vomiting
<br>- Skin rash, which appears two to five days after the onset of fever
<br>- Mild bleeding (such a nose bleed, bleeding gums, or easy bruising)
</p>
<br> The alternative name for dengue, "breakbone fever", comes from the associated muscle and joint pains.The course of infection 
 is divided into three phases: febrile, critical, and recovery.
</p>
<p>
The <i>febrile phase  </i>involves high fever, potentially over 40 °C (104 °F), and is associated with generalized pain and a headache; 
this usually lasts two to seven days. Nausea and vomiting may also occur.A rash occurs in 50–80% of those with symptoms in the first 
or second day of symptoms as flushed skin, or later in the course of illness (days 4–7), as a measles-like rash.A rash described as 
"islands of white in a sea of red" has also been observed. Some petechiae (small red spots that do not disappear when the skin is pressed,
 which are caused by broken capillaries) can appear at this point,[10] as may some mild bleeding from the mucous membranes of the mouth 
 and nose. The fever itself is classically biphasic or saddleback in nature, breaking and then returning for one or two days.</p>
