<h1 class="text-center">Diagnosis</h1>
<p>The diagnosis of dengue is typically made clinically, on the basis of reported symptoms and physical examination; this applies especially in endemic areas. However, early disease can be difficult to differentiate from other viral infections. A probable diagnosis is based on the findings of fever plus two of the following: nausea and vomiting, rash, generalized pains, low white blood cell count, positive tourniquet test, or any warning sign (see table) in someone who lives in an endemic area.Warning signs typically occur before the onset of severe dengue.The tourniquet test, which is particularly useful in settings where no laboratory investigations are readily available, involves the application of a blood pressure cuff at between the diastolic and systolic pressure for five minutes, followed by the counting of any petechial hemorrhages; a higher number makes a diagnosis of dengue more likely with the cut off being more than 10 to 20 per 1 inch2 (6.25 cm2).</p>
<p>
The diagnosis should be considered in anyone who develops a fever within two weeks of being in the tropics or subtropics. It can be difficult to distinguish dengue fever and chikungunya, a similar viral infection that shares many symptoms and occurs in similar parts of the world to dengue.Often, investigations are performed to exclude other conditions that cause similar symptoms, such as malaria, leptospirosis, viral hemorrhagic fever, typhoid fever, meningococcal disease, measles, and influenza.</p>
<p>
The earliest change detectable on laboratory investigations is a low white blood cell count, which may then be followed by low platelets and metabolic acidosis. A moderately elevated level of aminotransferase (AST and ALT) from the liver is commonly associated with low platelets and white blood cells. In severe disease, plasma leakage results in hemoconcentration (as indicated by a rising hematocrit) and hypoalbuminemia. Pleural effusions or ascites can be detected by physical examination when large, but the demonstration of fluid on ultrasound may assist in the early identification of dengue shock syndrome. The use of ultrasound is limited by lack of availability in many settings. Dengue shock syndrome is present if pulse pressure drops to â‰¤ 20 mm Hg along with peripheral vascular collapse.[9] Peripheral vascular collapse is determined in children via delayed capillary refill, rapid heart rate, or cold extremities. While warning signs are an important aspect for early detection of potential serious disease, the evidence for any specific clinical or laboratory marker is weak.</p>
