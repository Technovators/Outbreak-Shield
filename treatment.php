<h1 class="text-center">Treatment/Prevention</h1>
<p>
There are no approved vaccines for the dengue virus.Prevention thus depends on control of and protection from the bites of the mosquito 
that transmits it.The World Health Organization recommends an Integrated Vector Control program consisting of five elements:
</p>
<p>- Advocacy, social mobilization and legislation to ensure that public health bodies and communities are strengthened</p>
- Collaboration between the health and other sectors (public and private)<br>
- An integrated approach to disease control to maximize use of resources<br>
- Evidence-based decision making to ensure any interventions are targeted appropriately and<br>
- Capacity-building to ensure an adequate response to the local situation.<br>
<p>The primary method of controlling A. aegypti is by eliminating its habitats.This is done by getting rid of open sources of water, or if this is not possible, by adding insecticides or biological control agents to these areas. Generalized spraying with organophosphate or pyrethroid insecticides, while sometimes done, is not thought to be effective.Reducing open collections of water through environmental modification is the preferred method of control, given the concerns of negative health effects from insecticides and greater logistical difficulties with control agents.People can prevent mosquito bites by wearing clothing that fully covers the skin, using mosquito netting while resting, and/or the application of insect repellent (DEET being the most effective).However, these methods appear not to be sufficiently effective, as the frequency of outbreaks appears to be increasing in some areas, probably due to urbanization increasing the habitat of A. aegypti. The range of the disease appears to be expanding possibly due to climate change.
</p>
<h2> Dengue and Low Platelet Count </h2>
<p> Dengue is characteristically associated with a decrease in platelet count. The reduction in the count occurs at about 
three days from the on-set of fever. When the virus through the mosquito bite infects a person, the virus tends to bind to the 
White Blood Cells and then enters them. After this, they start reproducing inside the cells and do so while circulating throughout
 the body. Due to the bodyâ€™s immune response, the White Blood Cells produce signalling proteins such as cytokines and interferons,
  which cause the various symptoms such as fever and severe body pain associated with Dengue. In case of severe infection, the viral 
  reproduction rate is increased manifold and vital organs such as liver and bone marrow are affected. Capillary permeability is increased
   in the small blood vessels; as a result fluid tends to leak through the vessel walls and into the body cavity. Less blood circulates
    in the vessel due to this and the blood pressure drops very low. Furthermore, the infection of the bone marrow results in low platelet
     count, which leads to risk of relentless bleeding, as platelets help in clotting of the blood during injuries.
     </p>
